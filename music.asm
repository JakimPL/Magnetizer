EnableSound:
    LDA #%00000111
    STA SND_CHN
	RTS

PlaySound:
    LDA #%00111000
    STA PULSE1_TIMER
    LDA #$F4
    STA PULSE1_ENVELOPE
    LDA #$00
    STA PULSE1_SWEEP

    LDA #%01110110
    STA PULSE2_TIMER
    LDA #$F3
    STA PULSE2_ENVELOPE
    LDA #$00
    STA PULSE2_SWEEP

    LDA #%10000001
    STA TRIANGLE_TIMER
    LDA #$A2
    STA TRIANGLE_ENVELOPE
    LDA #$02
    STA TRIANGLE_SWEEP
	RTS
